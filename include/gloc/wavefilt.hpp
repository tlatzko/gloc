/**
 * @brief The wavelet class contains all required coefficient and the name of
 * the wavelet: supported wavelet families are: daub, sym and biorth
 *
 */
template <class real = double> class Wavelet {

protected:
  size_t filterLength_;
  std::string name_;
  std::vector<real> lowPassDecomposition_;
  std::vector<real> highPassDecomposition_;
  std::vector<real> lowPassReconstruction_;
  std::vector<real> highPassReconstruction_;

public:
  Wavelet(const std::string &name) : name_(name) {
    this->filterLength_ = filtlength(name);
    this->lowPassDecomposition_.resize(this->filterLength_);
    this->highPassDecomposition_.resize(this->filterLength_);
    this->lowPassReconstruction_.resize(this->filterLength_);
    this->highPassReconstruction_.resize(this->filterLength_);

    filtcoef(name, lowPassDecomposition_, highPassDecomposition_,
             lowPassReconstruction_, highPassReconstruction_);
  }

  const std::vector<real> &getLowPassDec() {
    return this->lowPassDecomposition_;
  }

  const std::vector<real> &getHighPassDec() {
    return this->lowPassDecomposition_;
  }

  const std::vector<real> &getLowPassRec() {
    return this->lowPassDecomposition_;
  }

  const std::vector<real> &getHighPassRec() {
    return this->lowPassDecomposition_;
  }
};

